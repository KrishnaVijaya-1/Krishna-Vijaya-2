class StringConcat
{
  public static void main(String args[])
   {
     String str1="Hello,I am";
     String str2="Krishna Vijaya";
     System.out.println(str1.concat(str2));
     System.out.println(str2.concat(str1));
     System.out.println(str1);
     System.out.println(str2);
   }
}
       